package lessons;

public class MathOperators {

	public static void main(String[] args) {
		// Math operations: +, -, *, /, %
		
		// Addition
		System.out.println(9 + 7);
		
		// Subtraction
		System.out.println(9 - 7);
		
		// Multiplication
		System.out.println(9 * 7);
		
		// Integer division - returns integer, truncates (doesn't round)
		System.out.println(9 / 7);
		
		// Division with double and integer - returns double
		System.out.println(9.0 / 7);
		
		// Division with 2 doubles - returns double
		System.out.println(7.9 / 7.3);
		
		// Modulus returns remainder of a division operation
		System.out.println(23 % 5);
	}

}
